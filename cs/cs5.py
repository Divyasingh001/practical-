#WAp to create a set 
set1 = set()
print("Initial blank Set: ")
print(set1)
  

set1 = set("GeeksForGeeks")
print("\nSet with the use of String: ")
print(set1)
  
String = 'GeeksForGeeks'
set1 = set(String)
print("\nSet with the use of an Object: " )
print(set1)
  
set1 = set(["Geeks", "For", "Geeks"])
print("\nSet with the use of List: ")
print(set1)
